package daily_assignments.assignment5;

public class Question10_NestedLoop {
	
	public static void main(String[] args) {
		
		
		
		for (int i=1; i<=7; i++) {
			
			for(int k=1; k<=i;  k++) {
			
				System.out.print(i);
				
				
			}
			System.out.println();
			
		}
	
		
		
		
		
		
		
		
		
		
		
}

}
